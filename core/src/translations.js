function t(key, vars = {}) {
    const lang = settings && settings.lang || "ru";
    let str = translations[lang][key] || translations['ru'][key] || key;
    Object.keys(vars).forEach(k => {
        str = str.replace("{" + k + "}", vars[k]);
    });
    return str;
}